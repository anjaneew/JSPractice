const PI = 3.14;

//== Equality Operator
if (PI == "3.14"){
    console.log("This is PI.");
}
else {
    console.log("This is NOT PI.");
}

//=== Strict Equality Operator

if (PI === "3.14"){
    console.log("This is PI.");
}
else {
    console.log("This is NOT PI.");
}

//!= Equality Operator
if (PI != "3.14"){
    console.log("This is NOT PI.");
}
else {
    console.log("This is PI.");
}

//!== Equality Operator
if (PI !== "3.14"){
    console.log("This is NOT PI.");
}
else {
    console.log("This is PI.");
}